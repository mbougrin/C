#include <string.h>

void			ft_bzero(void *s, size_t n)
{
	char	*ptr;

	ptr = (char *)s;
	if (n == 0)
		return ;
	while (n > 0)
	{
		*ptr = '\0';
		ptr++;
		n--;
	}
}
